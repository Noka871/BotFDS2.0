        f"–ü—Ä–∏–≤–µ—Ç, {user.first_name}!\n"
        "–Ø –±–æ—Ç –¥–ª—è —É—á–µ—Ç–∞ —Å–¥–∞—á–∏ –∞—É–¥–∏–æ–¥–æ—Ä–æ–∂–µ–∫.\n"
